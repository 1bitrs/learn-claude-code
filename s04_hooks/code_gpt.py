HOOKS = {"UserPromptSubmit": [], "PreToolUse": [], "PostToolUse": [], "Stop": []}


def register_hook(event: str, callback):
    HOOKS[event].append(callback)


def trigger_hooks(event: str, *args):
    for callback in HOOKS[event]:
        result = callback(*args)
        if result is not None:  # teaching shortcut: block this tool call
            return result
    return None


# s03 permission check logic, now wrapped as a hook
DENY_LIST = ["rm -rf /", "sudo", "shutdown", "reboot", "mkfs", "dd if="]
DESTRUCTIVE = ["rm ", "> /etc/", "chmod 777"]


def permission_hook(call: ChatCompletionMessageToolCall):
    """PreToolUse: s03 check_permission() logic moved here."""
    kwargs = json.loads(call.function.arguments)
    if call.function.name == "bash":
        for pattern in DENY_LIST:
            if pattern in kwargs.get("command", ""):
                print(f"\n\033[31m⛔ Blocked: '{pattern}'\033[0m")
                return "Permission denied by deny list"
        for kw in DESTRUCTIVE:
            if kw in kwargs.get("command", ""):
                print(f"\n\033[33m⚠  Potentially destructive command\033[0m")
                print(f"   Tool: {call.function.name}({kw})")
                choice = input("   Allow? [y/N] ").strip().lower()
                if choice not in ("y", "yes"):
                    return "Permission denied by user"
    if call.function.name in ("write_file", "edit_file"):
        path = kwargs.get("path", "")
        if not (WORKDIR / path).resolve().is_relative_to(WORKDIR):
            print(f"\n\033[33m⚠  Writing outside workspace\033[0m")
            print(f"   Tool: {call.function.name}({call.function.arguments})")
            choice = input("   Allow? [y/N] ").strip().lower()
            if choice not in ("y", "yes"):
                return "Permission denied by user"
    return None


def log_hook(call: ChatCompletionMessageToolCall):
    """PreToolUse: log every tool call."""
    print(
        f"\033[90m[HOOK] {call.function.name}({call.function.arguments[:120]})\033[0m"
    )
    return None


def large_output_hook(call: ChatCompletionMessageToolCall, output: str):
    """PostToolUse: warn on large output."""
    if len(output) > 100000:
        print(
            f"\033[33m[HOOK] ⚠ Large output from {call.function.name}: {len(output)} chars\033[0m"
        )
    return None


# UserPromptSubmit hook: log user input before it reaches the LLM
def context_inject_hook(query: str):
    print(f"\033[90m[HOOK] UserPromptSubmit: working in {WORKDIR}\033[0m")
    return None


# Stop hook: print summary when loop is about to exit
def summary_hook(messages: list):
    tool_count = sum(1 for m in messages if m.get("role") == "tool")
    print(f"\033[90m[HOOK] Stop: session used {tool_count} tool calls\033[0m")
    return None


register_hook("UserPromptSubmit", context_inject_hook)
register_hook("PreToolUse", permission_hook)
register_hook("PreToolUse", log_hook)
register_hook("PostToolUse", large_output_hook)
register_hook("Stop", summary_hook)


# ═══════════════════════════════════════════════════════════
#  agent_loop — same structure as s03, but no hard-coded check
#  s03: if not check_permission(block): ...
#  s04: if trigger_hooks("PreToolUse", block): ...
# ═══════════════════════════════════════════════════════════


def agent_loop(messages: list):
    while True:
        response = completion(
            model=MODEL,
            tools=TOOLS,
            max_tokens=8000,
            messages=messages,
        )
        response = cast(ModelResponse, response)

        msg = response.choices[0].message
        assistant_msg = {
            "role": "assistant",
            "content": msg.content or "",
        }  # type: Dict[str, Any]
        if msg.tool_calls:
            assistant_msg["tool_calls"] = [
                {
                    "id": c.id,
                    "type": "function",
                    "function": {
                        "name": c.function.name,
                        "arguments": c.function.arguments,
                    },
                }
                for c in msg.tool_calls
            ]
        messages.append(assistant_msg)

        if response.choices[0].finish_reason != "tool_calls":
            force = trigger_hooks("Stop", messages)
            if force:
                messages.append({"role": "user", "content": force})
                continue
            return

        for call in msg.tool_calls or []:
            print(
                f"\033[33m$ {call.function.name} {call.function.arguments[:120]}\033[0m"
            )
            # s04 change: hook replaces hard-coded check_permission()
            blocked = trigger_hooks("PreToolUse", call)
            if blocked:
                messages.append(
                    {
                        "role": "tool",
                        "tool_call_id": call.id,
                        "content": blocked or "",
                    }
                )
                continue
            handler = TOOL_HANDLERS.get(call.function.name or "")
            kwargs = json.loads(call.function.arguments)
            output = handler(**kwargs) if handler else f"Unknown: {call.function.name}"
            trigger_hooks("PostToolUse", call, output)  # s04: post hook
            messages.append(
                {
                    "role": "tool",
                    "tool_call_id": call.id,
                    "content": output,
                }
            )


